package by.tms.DomashneeZadanie._4_Calculator.Calculator;

import java.util.Scanner;

public class Calc implements CalcIN{


    public int calc(int num1, int num2, char operation){
        int result;
        switch (operation){
            case '+':
                result = num1+num2;
                break;
            case '-':
                result = num1-num2;
                break;
            case '*':
                result = num1*num2;
                break;
            case '/':
                result = num1/num2;
                break;
            default:
                System.out.println("Операция не распознана. Повторите ввод.");
                result = calc(num1, num2, operation);

        }
        return result;
    }


}