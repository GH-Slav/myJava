package by.tms._Lessons_.Ch03._1_Типы_данных_и_переменные;

public class Main {
    public static void main(String[] args) {
        //public - публичный модификатор;
        //static - статичный модификатор;
        //void - метод ничего не возвращает;
        //main - название метода;
        //String[] args - тело метода;
        int num1 = 100;
        int num2 = 30 * 110;
        int num3 = num2 - 150;
        System.out.println(" num: " + num1  + " " + num2 + " " + num3 + " " + 200);
        System.out.println("Пишем что хотим");
        System.out.println("Привет Кто-то\n Java");
        System.out.println("Привет Кто-то\t Java");
        System.out.println("Привет Кто-то\b Java");
        System.out.println("Привет Кто-то\f Java");
        System.out.println("Привет Кто-то\r Java");
        System.out.println("\"Привет Кто-то\' Java");
        System.out.println("Привет Кто-то\\ Java");
    }
}
